console.log("PI:", Math.PI);                // 3.141592653589793
console.log("Round:", Math.round(4.7));     // 5
console.log("Floor:", Math.floor(4.7));     // 4
console.log("Ceil:", Math.ceil(4.3));       // 5
console.log("Absolute:", Math.abs(-10));    // 10
console.log("Power:", Math.pow(2, 3));      // 8
console.log("Square root:", Math.sqrt(16)); // 4
console.log("Max:", Math.max(1, 5, 3));     // 5
console.log("Min:", Math.min(1, 5, 3));     // 1
console.log("Random:", Math.random());      // random number between 0 and 1
console.log("Truncate:", Math.trunc(4.9));  // 4
console.log("Sign:", Math.sign(-5));        // -1
console.log("Sin(PI/2):", Math.sin(Math.PI/2)); // 1
